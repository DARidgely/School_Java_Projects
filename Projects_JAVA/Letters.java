public class Letters{

      public static void main(String[] args){
      
            final int ROWS = 5;
            final int COLS = 1;
      
            
            for(int rows = 0; rows < ROWS; rows++){
                  
                  for(int cols = 0; cols < COLS; cols++){
                     System.out.print("a");
                     System.out.print("");
                        
                  }
                  System.out.println();
            }
            
            
            
      }
      
}